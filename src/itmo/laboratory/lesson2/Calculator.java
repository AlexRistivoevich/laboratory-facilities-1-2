package itmo.laboratory.lesson2;

public class Calculator {


    public static int addition(int a, int b, int c) {
        int sum;
        sum = a + b + c;
        return sum;
    }

    public static double addition(double a, double b, double c) {
        double sum;
        sum = a + b + c;
        return sum;
    }

    public static long addition(long a, long b, long c) {
        long sum;
        sum = a + b + c;
        return sum;
    }

    public static double division(int a, int b, int c) {
        double sum;
        sum = ((double) a / b / c);
        return sum;
    }

    public static double division(double a, double b, double c) {
        double sum;
        sum = a / b / c;
        return sum;
    }

    public static long division(long a, long b, long c) {
        long sum;
        sum = a / b / c;
        return sum;
    }


    public static int multiplication(int a, int b, int c) {
        int sum;
        sum = a * b * c;
        return sum;
    }

    public static double multiplication(double a, double b, double c) {
        double sum;
        sum = a * b * c;
        return sum;
    }

    public static long multiplication(long a, long b, long c) {
        long sum;
        sum = a * b * c;
        return sum;
    }


    public static int subtraction(int a, int b, int c) {
        int sum;
        sum = a - b - c;
        return sum;
    }

    public static double subtraction(double a, double b, double c) {
        double sum;
        sum = a - b - c;
        return sum;
    }

    public static long subtraction(long a, long b, long c) {
        long sum;
        sum = a - b - c;
        return sum;
    }
}
